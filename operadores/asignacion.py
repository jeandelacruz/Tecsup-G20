

# Operador asignación
# Igual (=)
x = 10
print(f'Asignación - Igual: {x}')

# Incremento (+=)
x += 2  # x = x + 2
print(f'Asignación - Incremento: {x}')

# Reducir (-=)
x -= 4  # x = x - 4
print(f'Asignación - Reducir: {x}')

# Multiplicar (*=)
x *= 2  # x = x * 2
print(f'Asignación - Multiplicar: {x}')
